    public void showCars(){
        ArrayList<Car> cars = new ArrayList<Car>();
        
        model.setRowCount(0);
        
        cars = equations.insertCars();
        
        if(cars != null){
            
            for(Car car : cars){
                Object[] newcars = {car.getId(),car.getMake(),car.getBrand(),car.getModel(),car.getPlate(),car.getCarYear(),car.getCarType(),car.getDailyPrice(),car.getAtCustomer()};
                model.addRow(newcars);
            }
        }
        
        
        
    }
