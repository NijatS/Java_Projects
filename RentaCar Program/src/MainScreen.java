    public void dinamicSearch(String search){
        
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<DefaultTableModel>(model);
        
        car_table.setRowSorter(trs);
        
        trs.setRowFilter(RowFilter.regexFilter(search));
        
    }
    private void search_areaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_areaKeyReleased
        String search = search_area.getText();
        
        dinamicSearch(search);
    }//GEN-LAST:event_search_areaKeyReleased

    private void delete_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_buttonActionPerformed
       error_area.setText("");
        int selectedrow  = car_table.getSelectedRow();
         if(selectedrow == -1){
             if(model.getRowCount() == 0){
                 error_area.setText("Please insert any cars..");
             }
             error_area.setText("Please select any row...");
             
         }
         else{
             
             int id = (int)model.getValueAt(selectedrow, 0);
             equations.deleteCar(id);
             showCars();
         }
        
    }//GEN-LAST:event_delete_buttonActionPerformed

    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
         error_area.setText("");
       String make = make_area.getText();
       String brand = brand_area.getText();
       String model = model_area.getText();
       String plate = plate_area.getText();
       int year = valueOf(year_area.getText());
       String type = type_area.getText();
       int price = valueOf(price_area.getText());
       Boolean atCustomer = Boolean.valueOf((working_area.getText()));
       
       equations.addCar(make,brand,model,plate,year,type,price,atCustomer);
       error_area.setText("The car is added...");
       
       showCars();
        
    }//GEN-LAST:event_add_buttonActionPerformed

